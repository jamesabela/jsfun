# Level 7 - Loop Through Colours
# Puzzle: print each colour with 'Colour: ' before it, not the whole list.

colours = ["red", "green", "blue", "purple"]

for colour in colours:
    print(colours)

#Input
# ""

#output
# Colour: red, Colour: green, Colour: blue, Colour: purple

