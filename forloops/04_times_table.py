# Level 4 - Times Table
# Ask the user for a number.
# Print the first five multiples of that number.

number = int(input("Choose a number: "))

for i in range(1, 6):
    print(number * i)

#Input
# 3
# 7
# 10

#output
# 3
# 6
# 9

# 7
# 14
# 21

# 10
# 20
# 30

